    private void btnCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckActionPerformed
        // TODO add your handling code here:
        try {
            if(!jtfTranslate.equals("")){
                if(word.Translate.equals((String)jtfTranslate.getText())){
                    JOptionPane.showMessageDialog(this, "your answer is correct",
                        "YOUR ANSER",JOptionPane.INFORMATION_MESSAGE);
                    rand();
                    jtfTranslate.setText("");
                }else{
                    JOptionPane.showMessageDialog(this, "your answer is wrong",
                    "YOUR ANSER",JOptionPane.INFORMATION_MESSAGE);
                    rand();
                    jtfTranslate.setText("");
                }
            }else{
                JOptionPane.showMessageDialog(this, "You haven't entered an answer yet.",
                        "YOUR ANSER",JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnCheckActionPerformed

